CREATE PROCEDURE [dbo].[sp_ListarAtencionesPorFechas]
    @Desde DATE,
    @Hasta DATE
AS
BEGIN
    SELECT 
        C.Nombre,
        C.Apellidos,
        M.DESCRIPCION AS Motivo,
        A.Fecha_Alta
    FROM ATENCION_CLIENTE A
    INNER JOIN CLIENTE C ON A.CLIENTE_ID = C.Id
    INNER JOIN MOTIVO M ON A.MOTIVO_ID = M.Id
    WHERE cast(A.Fecha_Alta as date) BETWEEN @Desde AND @Hasta
    ORDER BY A.FECHA_ALTA DESC;
END
GO
