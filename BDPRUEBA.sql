CREATE PROCEDURE [dbo].[sp_VerificarAtencionHoy]
    @Celular VARCHAR(20)
AS
BEGIN
    SELECT ac.Id, c.Nombre, c.Apellidos, m.Descripcion AS Motivo, ac.Fecha_Alta
    FROM ATENCION_CLIENTE ac
    INNER JOIN CLIENTE c ON c.Id = ac.Cliente_Id
    INNER JOIN MOTIVO m ON m.Id = ac.Motivo_Id
    WHERE c.Celular = @Celular
      AND CAST(ac.Fecha_Alta AS DATE) = CAST(GETDATE() AS DATE)
END
